CREATE TABLE `tucao`.`tc_users` (
  `USER_ID` INT NOT NULL AUTO_INCREMENT,
  `GENDER` TINYINT(4) NULL DEFAULT 0,
  `NICK_NAME` VARCHAR(50) NULL,
  `REG_PHONE_NUM` VARCHAR(50) NULL,
  `REG_EMAIL` VARCHAR(100) NULL,
  `PASSWORD` VARCHAR(50) NOT NULL,
  `LEVEL` INT NULL DEFAULT 0,
  `SCORE` INT NULL DEFAULT 0,
  `USER_STATUS` INT NULL,
  `DEFAULT_PIC` INT NULL,
  `HEAD_PIC` VARCHAR(100) NULL,
  PRIMARY KEY (`USER_ID`),
  UNIQUE INDEX `USER_ID_UNIQUE` (`USER_ID` ASC),
  UNIQUE INDEX `REG_PHONE_NUM_UNIQUE` (`REG_PHONE_NUM` ASC),
  UNIQUE INDEX `REG_EMAIL_UNIQUE` (`REG_EMAIL` ASC));
